// Diferencias entre un Método y una Función

const numero1 = 20;
const numero2 = "20";

console.log( parseInt(numero2) ); //parseInt es una función
console.log( numero1.toString() ); // .toString es un método
// Si tiene un . seguido del nombre de la función es un método.
// Si tiene el nombre de la función seguido de () son funciones.